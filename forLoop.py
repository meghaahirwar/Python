#For loop
#Definition: A for loop is used for iterating over a sequence (that is either a list, a tuple, a dictionary, a set, or a string).

#range() function in loop
range(5)
# start = 0
# condition < 5
# increament = bydefault +1
#0, 1, 2, 3, 4

range(1, 6)
# start = 1
# condition < 6
# increament = bydefault +1
#1, 2, 3, 4, 5

range(1, 6, 2)
# start = 1
# condition < 6
# increament = bydefault +2
#1, 3,  5

#for loop execution
for x in range(5):
    print(x)
print()

for x in range(1, 5):
    print(x)
print()
   
for x in range(1, 5, 2):
    print(x)
print()
   
for x in range(5):
    print("Welcome")


